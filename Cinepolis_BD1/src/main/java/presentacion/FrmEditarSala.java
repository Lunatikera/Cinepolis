    public void metodoMostrarDatos() throws NegocioException{
        try{
        SalaDTO c = this.salaBO.leerPorId(id);
            String nombre = c.getNombre();
            int numeroA = c.getNumeroAsiento();
            int duracionL = c.getDuracionLimpieza();
            TxtNombre.setText(nombre);
            TxtNumeroAsiento.setText(String.valueOf(numeroA));
            TxtDuracion.setText(String.valueOf(duracionL));
        }catch(NegocioException ex){
            System.out.println(ex.getMessage());
        } 
    };
