    private void BtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarActionPerformed
            String nombre = TxtNombre.getText();
            int numA = Integer.parseInt(TxtNumeroAsiento.getText());
            int duracion = Integer.parseInt(TxtDuracion.getText());
            
            if (nombre.isEmpty() || numA == 0 || duracion == 0) {
             JOptionPane.showMessageDialog(this, "Ingrese todos los campos obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
           
        }
        try
        {
            SalaDTO c = this.salaBO.leerPorId(id);
            SalaDTO editar = new SalaDTO(c.getId(),nombre,numA,duracion,idSucursal,c.isEstaEliminada());
            this.salaBO.editar(editar);
            JOptionPane.showMessageDialog(this, "Registro modificado correctamente.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        } catch (NegocioException ex)
        {
            Logger.getLogger(FrmEditarSala.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
        
    }//GEN-LAST:event_BtnAceptarActionPerformed


